#include "Position.h"

////////////////////////////////////////////////////////////
//      Constructor
////////////////////////////////////////////////////////////

Position::Position() {

    letter = 'A';
    number = 1;
}

////////////////////////////////////////////////////////////
//      Constructor with Parameters
////////////////////////////////////////////////////////////

Position::Position(char letter, int number) {

    this->letter = letter;
    this->number = number;
}

////////////////////////////////////////////////////////////
//      Getters
////////////////////////////////////////////////////////////

const char& Position::getLetter() { return letter; }
const int& Position::getNumber() { return number; }

////////////////////////////////////////////////////////////
//      Assignment Operator Overload
////////////////////////////////////////////////////////////

Position& Position::operator=(Position& otherPosition) {

    letter = otherPosition.getLetter();
    number = otherPosition.getNumber();

    return *this;
}

////////////////////////////////////////////////////////////
//      Equality Operator Overload
////////////////////////////////////////////////////////////

bool Position::operator==(Position& otherPosition) {

    if ((letter == otherPosition.getLetter()) && number == otherPosition.getNumber()) {

        return true;
    }
    else { return false; }
}