////////////////////////////////////////////////////////////
//      Convert Letter to Number
////////////////////////////////////////////////////////////

int convertLetterToNumber(const char& element) {

    if (element == 'A') { return 1; }
    else if (element == 'B') { return 2; }
    else if (element == 'C') { return 3; }
    else if (element == 'D') { return 4; }
    else if (element == 'E') { return 5; }
    else if (element == 'F') { return 6; }
    else if (element == 'G') { return 7; }
    else if (element == 'H') { return 8; }
    else if (element == 'I') { return 9; }
    else if (element == 'J') { return 10; }
    else { return -1; }
}

