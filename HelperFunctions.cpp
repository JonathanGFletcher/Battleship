Position checkPositionValidity(Position position) {

    if (

        (
            position.getLetter() == 'A' ||
            position.getLetter() == 'B' ||
            position.getLetter() == 'C' ||
            position.getLetter() == 'D' ||
            position.getLetter() == 'E' ||
            position.getLetter() == 'F' ||
            position.getLetter() == 'G' ||
            position.getLetter() == 'H' ||
            position.getLetter() == 'I' ||
            position.getLetter() == 'J'
        ) && (

            position.getNumber() >= 1 &&
            position.getNumber() <= 10
        )
    ) {

        return position;
    }
    else {

        Position blank('_', 0);

        return blank;
    }
}